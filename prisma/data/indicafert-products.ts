function gstFromHsn(hsn?: string): number {
  if (!hsn) return 5;
  if (hsn.startsWith("3105") || hsn.startsWith("3104")) return 5;
  return 18;
}

function p(
  partial: Omit<IndicafertProductInput, "gstRate"> & { gstRate?: number }
): IndicafertProductInput {
  return {
    ...partial,
    gstRate: partial.gstRate ?? gstFromHsn(partial.hsnCode),
    status: partial.status ?? (partial.basePrice > 0 ? "ACTIVE" : "INACTIVE"),
  };
}

